import 'package:flutter/material.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:intl/intl.dart';
import '../../models/purchase.dart';

class AddPurchaseScreen extends StatefulWidget {
  const AddPurchaseScreen({super.key});

  @override
  State<AddPurchaseScreen> createState() => _AddPurchaseScreenState();
}

class _AddPurchaseScreenState extends State<AddPurchaseScreen> {
  DateTime _selectedDate = DateTime.now();
  String? _selectedSupplierId;
  String? _selectedSupplierName;

  // Purchase Invoice Number
  String _invoiceNumber = '';
  final TextEditingController _invoiceController = TextEditingController();
  bool _isInvoiceAutoGenerated = true;

  // Cart items
  List<CartItem> _cartItems = [];

  // Single product selection
  String? _selectedProductId;
  String? _selectedProductName;
  final TextEditingController _purchaseRateController = TextEditingController();
  final TextEditingController _quantityController = TextEditingController();

  // Rounding amount
  final TextEditingController _roundingController = TextEditingController(
    text: '0',
  );
  double _roundingAmount = 0;

  bool _isLoading = false;
  double _productRate = 0;
  double _productSaleRate = 0;
  int _productGst = 18;

  List<QueryDocumentSnapshot> _suppliers = [];
  List<QueryDocumentSnapshot> _products = [];

  final FirebaseFirestore _firestore = FirebaseFirestore.instance;

  @override
  void initState() {
    super.initState();
    _loadSuppliers();
    _loadProducts();
    _generateInvoiceNumber();
    _roundingController.addListener(_updateRounding);
  }

  void _updateRounding() {
    setState(() {
      _roundingAmount = double.tryParse(_roundingController.text) ?? 0;
    });
  }

  // Generate Invoice Number: INV-YYYYMMDD-XXX
  Future<void> _generateInvoiceNumber() async {
    try {
      String datePrefix = DateFormat('yyyyMMdd').format(DateTime.now());

      // Get count of purchases for today
      QuerySnapshot todayPurchases = await _firestore
          .collection('purchases')
          .where(
            'date',
            isGreaterThanOrEqualTo: Timestamp.fromDate(
              DateTime(
                DateTime.now().year,
                DateTime.now().month,
                DateTime.now().day,
              ),
            ),
          )
          .where(
            'date',
            isLessThan: Timestamp.fromDate(
              DateTime(
                DateTime.now().year,
                DateTime.now().month,
                DateTime.now().day + 1,
              ),
            ),
          )
          .get();

      int count = todayPurchases.docs.length + 1;
      String counter = count.toString().padLeft(3, '0');

      setState(() {
        _invoiceNumber = 'INV-$datePrefix-$counter';
        _invoiceController.text = _invoiceNumber;
      });
    } catch (e) {
      // Fallback: Generate based on timestamp
      String timestamp = DateTime.now().millisecondsSinceEpoch
          .toString()
          .substring(8);
      setState(() {
        _invoiceNumber =
            'INV-${DateFormat('yyyyMMdd').format(DateTime.now())}-$timestamp';
        _invoiceController.text = _invoiceNumber;
      });
    }
  }

  void _showDialog(String title, String message, {bool isError = false}) {
    showDialog(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.circular(16),
          ),
          title: Row(
            children: [
              Icon(
                isError ? Icons.error_outline : Icons.check_circle_outline,
                color: isError ? Colors.red : Colors.green,
                size: 22,
              ),
              const SizedBox(width: 8),
              Text(
                title,
                style: const TextStyle(
                  fontWeight: FontWeight.bold,
                  fontSize: 15,
                ),
              ),
            ],
          ),
          content: Text(message, style: const TextStyle(fontSize: 13)),
          actions: [
            TextButton(
              onPressed: () => Navigator.of(context).pop(),
              style: TextButton.styleFrom(foregroundColor: Colors.green[700]),
              child: const Text('OK', style: TextStyle(fontSize: 13)),
            ),
          ],
        );
      },
    );
  }

  Future<void> _loadSuppliers() async {
    try {
      QuerySnapshot snapshot = await _firestore.collection('suppliers').get();
      setState(() => _suppliers = snapshot.docs);
    } catch (e) {
      _showDialog('Error', 'Error loading suppliers: $e', isError: true);
    }
  }

  Future<void> _loadProducts() async {
    try {
      QuerySnapshot snapshot = await _firestore.collection('products').get();
      setState(() => _products = snapshot.docs);
    } catch (e) {
      _showDialog('Error', 'Error loading products: $e', isError: true);
    }
  }

  void _onProductSelected(String? productId) {
    if (productId == null) return;

    try {
      final product = _products.firstWhere((doc) => doc.id == productId);
      final productData = product.data() as Map<String, dynamic>;

      setState(() {
        _selectedProductId = productId;
        _selectedProductName = (productData['productName'] ?? 'Unknown')
            .toString();
        _productRate = (productData['purchaseRate'] ?? 0).toDouble();
        _productSaleRate = (productData['saleRate'] ?? 0).toDouble();
        _productGst = (productData['gstPercentage'] ?? 18).toInt();
        _purchaseRateController.text = _productRate.toString();
        _quantityController.text = '1';
      });
    } catch (e) {
      _showDialog('Error', 'Error selecting product: $e', isError: true);
    }
  }

  void _addToCart() {
    if (_selectedProductId == null) {
      _showDialog('Error', 'Please select a product', isError: true);
      return;
    }

    if (_quantityController.text.trim().isEmpty) {
      _showDialog('Error', 'Please enter quantity', isError: true);
      return;
    }

    int quantity = int.tryParse(_quantityController.text.trim()) ?? 0;
    if (quantity <= 0) {
      _showDialog('Error', 'Please enter valid quantity', isError: true);
      return;
    }

    double rate = double.tryParse(_purchaseRateController.text.trim()) ?? 0;
    if (rate <= 0) {
      _showDialog('Error', 'Please enter valid purchase rate', isError: true);
      return;
    }

    int existingIndex = _cartItems.indexWhere(
      (item) => item.productId == _selectedProductId,
    );

    if (existingIndex != -1) {
      setState(() {
        _cartItems[existingIndex].quantity += quantity;
        _cartItems[existingIndex].total =
            _cartItems[existingIndex].rate * _cartItems[existingIndex].quantity;
      });
      _showDialog('Success', 'Product quantity updated in cart!');
    } else {
      setState(() {
        _cartItems.add(
          CartItem(
            productId: _selectedProductId!,
            productName: _selectedProductName!,
            rate: rate,
            quantity: quantity,
            total: rate * quantity,
            gstPercentage: _productGst,
          ),
        );
      });
      _showDialog('Success', 'Product added to cart!');
    }

    setState(() {
      _selectedProductId = null;
      _selectedProductName = null;
      _purchaseRateController.clear();
      _quantityController.clear();
      _productRate = 0;
      _productSaleRate = 0;
      _productGst = 18;
    });
  }

  void _removeFromCart(int index) {
    setState(() => _cartItems.removeAt(index));
    _showDialog('Success', 'Product removed from cart');
  }

  void _updateCartItemQuantity(int index, int newQuantity) {
    if (newQuantity <= 0) {
      _removeFromCart(index);
      return;
    }

    setState(() {
      _cartItems[index].quantity = newQuantity;
      _cartItems[index].total = _cartItems[index].rate * newQuantity;
    });
  }

  double _getSubtotal() => _cartItems.fold(0, (sum, item) => sum + item.total);
  double _getTotalGST() => _cartItems.fold(
    0,
    (sum, item) => sum + (item.total * item.gstPercentage / 100),
  );
  double _getGrandTotal() => _getSubtotal() + _getTotalGST() + _roundingAmount;

  Future<void> _savePurchase() async {
    if (_selectedSupplierId == null || _selectedSupplierId!.isEmpty) {
      _showDialog(
        'Validation Error',
        'Please select a supplier',
        isError: true,
      );
      return;
    }

    if (_cartItems.isEmpty) {
      _showDialog(
        'Validation Error',
        'Please add at least one product to cart',
        isError: true,
      );
      return;
    }

    // Validate invoice number
    if (_invoiceController.text.trim().isEmpty) {
      _showDialog(
        'Validation Error',
        'Please enter an invoice number',
        isError: true,
      );
      return;
    }

    // Check if invoice number already exists
    try {
      QuerySnapshot existingInvoice = await _firestore
          .collection('purchases')
          .where('invoiceNo', isEqualTo: _invoiceController.text.trim())
          .get();

      if (existingInvoice.docs.isNotEmpty) {
        _showDialog(
          'Validation Error',
          'Invoice number ${_invoiceController.text.trim()} already exists. Please use a different number.',
          isError: true,
        );
        return;
      }
    } catch (e) {
      // Continue with save if check fails
    }

    setState(() => _isLoading = true);

    try {
      // Create PurchaseItem objects using the updated model
      List<PurchaseItem> purchaseItems = _cartItems
          .map(
            (item) => PurchaseItem(
              productId: item.productId,
              productName: item.productName,
              rate: item.rate, // Using 'rate' as per updated model
              quantity: item.quantity,
              total: item.total,
              gstPercentage: item.gstPercentage,
            ),
          )
          .toList();

      await _firestore.collection('purchases').add({
        'supplierId': _selectedSupplierId,
        'supplierName': _selectedSupplierName ?? 'Unknown',
        'invoiceNo': _invoiceController.text.trim(),
        'date': Timestamp.fromDate(_selectedDate),
        'items': purchaseItems.map((item) => item.toMap()).toList(),
        'totalAmount': _getSubtotal(),
        'gstAmount': _getTotalGST(),
        'roundingAmount': _roundingAmount,
        'grandTotal': _getGrandTotal(),
        'createdAt': FieldValue.serverTimestamp(),
      });

      setState(() {
        _selectedSupplierId = null;
        _selectedSupplierName = null;
        _cartItems.clear();
        _selectedProductId = null;
        _selectedProductName = null;
        _quantityController.clear();
        _purchaseRateController.clear();
        _selectedDate = DateTime.now();
        _productRate = 0;
        _productSaleRate = 0;
        _productGst = 18;
        _roundingController.text = '0';
        _roundingAmount = 0;
      });

      // Generate new invoice number for next purchase
      await _generateInvoiceNumber();

      _showDialog('Success', 'Purchase added successfully!');
    } catch (e) {
      _showDialog(
        'Error',
        'Error saving purchase: ${e.toString()}',
        isError: true,
      );
    } finally {
      setState(() => _isLoading = false);
    }
  }

