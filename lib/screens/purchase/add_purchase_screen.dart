  Widget _buildSupplierDropdown() {
    return DropdownButtonFormField<String>(
      decoration: _buildInputDecoration('Select Supplier *', Icons.business),
      value: _selectedSupplierId,
      items: _suppliers.map((supplier) {
        Map<String, dynamic> data = supplier.data() as Map<String, dynamic>;
        return DropdownMenuItem(
          value: supplier.id,
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                data['name'] ?? 'Unknown',
                style: const TextStyle(fontSize: 12),
              ),
              if (data['gstNumber'] != null &&
                  data['gstNumber'].toString().isNotEmpty)
                Text(
                  'GST: ${data['gstNumber']}',
                  style: const TextStyle(fontSize: 10, color: Colors.grey),
                ),
            ],
          ),
        );
      }).toList(),
      onChanged: (value) {
        if (value != null) {
          final supplier = _suppliers.firstWhere((doc) => doc.id == value);
          Map<String, dynamic> data = supplier.data() as Map<String, dynamic>;
          setState(() {
            _selectedSupplierId = value;
            _selectedSupplierName = (data['name'] ?? 'Unknown').toString();
          });
        }
      },
      validator: (value) => value == null ? 'Please select a supplier' : null,
    );
  }

